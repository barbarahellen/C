#include<stdio.h>

int main(void){

    printf("hello world\n");

    // this is a comment

    /*
        escape sequence = character combination consisting of a backlash \
        followed by a letter or combination of digits
        \n = new line
        \t = tab

    */
    
    printf("1\t2\t3\n4\t5\t6\n");
   
   
    return 0;
}